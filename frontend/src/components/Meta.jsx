import React from 'react';
import { Helmet } from 'react-helmet';

//===================================================================================================================================================================//

// Component that allows for title and meta tags to be used on different pages.
const Meta = ({ title, description, keywords }) => {
    
    return (
        <Helmet>
            <title>{ title }</title>
            <meta name='description' content={ description }/>
            <meta name='keywords' content={ keywords }/>
        </Helmet>
    )
};

//===================================================================================================================================================================//

Meta.defaultProps = {
    title: 'Welcome To ShopNS',
    description: 'We sell the best products from around Nova Scotia',
    keywords: 'buy Nova Scotia products'
}

//===================================================================================================================================================================//

export default Meta;